package com.department.data.validation;

/**
 * Validates the messages.
 * 
 * @author  Rajkumar
 * @version 1.0
 */

public class ValidateMessage {

	private String errorMessage;
	private String errorCriteria;

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}

	public String getErrorCriteria() {
		return errorCriteria;
	}

	public void setErrorCriteria(String errorCriteria) {
		this.errorCriteria = errorCriteria;
	}

}
